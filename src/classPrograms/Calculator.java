package classPrograms;

public class Calculator {
	
	double addition(double n1, double n2) {
		return (n1 + n2);
	}
	
	double Subtraction(double n1, double n2) {
		return (n1 - n2);
	}
	
	double Multiplication(double n1, double n2) {
		return (n1*n2);
	}
	
	double Division(double n1, double n2) {
		return (n1/n2);
	}
}
